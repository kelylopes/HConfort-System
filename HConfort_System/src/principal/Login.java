    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      Connection con = Conexao.AbrirConexao();
        GerenteDAO sql = new GerenteDAO(con);
        String login = jTextField2.getText();
        String senha = jPasswordField1.getText();
        if (login.equalsIgnoreCase("") || senha.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Nenhum campo pode esta vazio" , "confort", JOptionPane.WARNING_MESSAGE);
            jTextField2.setText("");
            jPasswordField1.setText("");
            
        }else{
            if (sql.Logar(login, senha) == true){
                new Thread(){
                    public void run(){
                        for (int i = 0;  i < 101; i++){
                            jProgressBar1.setValue(i);
                            try {
                                Thread.sleep(35);
                            } catch (Exception ex){
                                ex.getMessage();
                            }
                        }
                        new Login().setVisible(true);
                        dispose();
                    }
                }.start();
                
            }else{
                JOptionPane.showMessageDialog(null, "Usu√°rio ou senha iv√°lidos" , "confort", JOptionPane.ERROR_MESSAGE);
                jTextField2.setText("");
                jPasswordField1.setText("");
                
            }
        }
      
            

    }//GEN-LAST:event_jButton2ActionPerformed

